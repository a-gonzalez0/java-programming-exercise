package chapter1;

/* 
 * (Area and perimeter of a rectangle) Write a program that displays the area and
perimeter of a rectangle with the width of 4.5 and height of 7.9 using the following
formula:
area = width * height
 */

public class Question09 {
	
	public static void main(String[] args) {
		System.out.println("The area of a rectangle with the width of 4.5 and height of 7.9 is: " + (4.5 * 7.9));
		System.out.println("The perimeter of a rectangle with the width of 4.5 and height of 7.9 is: " + ((4.5 * 2) + (7.9 * 2)));
	}
}
